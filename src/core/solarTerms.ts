// 通用節氣估算（針對表格外年份）
function estimateSolarTerm(year: number, month: number, day: number): string | null {
  // 粗略估算：每月兩個節氣約在5-7日和19-22日
  const approxTerms: Record<number, [number, string, number, string]> = {
    1: [5, '小寒', 20, '大寒'],
    2: [4, '立春', 19, '雨水'],
    3: [6, '驚蟄', 21, '春分'],
    4: [5, '清明', 20, '穀雨'],
    5: [5, '立夏', 21, '小滿'],
    6: [6, '芒種', 21, '夏至'],
    7: [7, '小暑', 23, '大暑'],
    8: [7, '立秋', 23, '處暑'],
    9: [7, '白露', 23, '秋分'],
    10: [8, '寒露', 23, '霜降'],
    11: [7, '立冬', 22, '小雪'],
    12: [7, '大雪', 22, '冬至'],
  };
  
  const [d1, t1, d2, t2] = approxTerms[month] || [5, '', 21, ''];
  if (day === d1) return t1;
  if (day === d2) return t2;
  return null;
}

export function getSolarTerm(year: number, month: number, day: number): string | null {
  const yearTable = TERM_TABLE[year];
  if (yearTable) {
    const monthTable = yearTable[month];
    if (monthTable && monthTable[day]) {
      return monthTable[day];
    }
    return null;
  }
  return estimateSolarTerm(year, month, day);
}

// 取得某月所有節氣日
export function getMonthSolarTerms(year: number, month: number): Record<number, string> {
  const yearTable = TERM_TABLE[year];
  if (yearTable && yearTable[month]) {
    return yearTable[month];
  }
  // fallback
  const result: Record<number, string> = {};
  const approxTerms: Record<number, [number, string, number, string]> = {
    1: [5, '小寒', 20, '大寒'],
    2: [4, '立春', 19, '雨水'],
    3: [6, '驚蟄', 21, '春分'],
    4: [5, '清明', 20, '穀雨'],
    5: [5, '立夏', 21, '小滿'],
    6: [6, '芒種', 21, '夏至'],
    7: [7, '小暑', 23, '大暑'],
    8: [7, '立秋', 23, '處暑'],
    9: [7, '白露', 23, '秋分'],
    10: [8, '寒露', 23, '霜降'],
    11: [7, '立冬', 22, '小雪'],
    12: [7, '大雪', 22, '冬至'],
  };
  if (approxTerms[month]) {
    const [d1, t1, d2, t2] = approxTerms[month];
    result[d1] = t1;
    result[d2] = t2;
  }
  return result;
}
